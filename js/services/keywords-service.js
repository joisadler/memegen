'use strict';

const gKeywords = {
  happy: 0,
  angry: 0,
  funny: 0,
  crazy: 0,
  sarcastic: 0,
  sad: 0,
  animal: 0,
};

// eslint-disable-next-line no-unused-vars
function getKeywordPopularity(word) {
  return gKeywords.word;
}

function getKeywordsForDisplay() {
  return [...Object.entries(gKeywords)];
}
